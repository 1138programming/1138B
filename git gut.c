void driveBase(int speed, int encoderValue) {
	int encoder = 0;
	motor[RightBase] = speed;
	motor[LeftBase] = speed;
	nMotorEncoder[LeftBase] = 0;
	nMotorEncoder[RightBase] = 0;
	//while ((nMotorEncoder[LeftBase] + nMotorEncoder[RightBase]) / 2 < encoderValue) {
	while(nMotorEncoder[LeftBase] < encoderValue) {
		encoder = nMotorEncoder[LeftBase];
		switch (compare(nMotorEncoder[LeftBase], nMotorEncoder[RightBase])) {
			case -1:
				// Right base is ahead; speed the left base up
				motor[LeftBase] = speed + 10;
				motor[RightBase] = speed;
				break;
			case 1:
				// Left base is ahead; speed the right base up
				motor[LeftBase] = speed;
				motor[RightBase] = speed + 10;
				break;
			default:
				// Set both to the default speed
				motor[LeftBase] = motor[RightBase] = speed;
				break;
		}
	}
	motor[RightBase] = 0;
	motor[LeftBase] = 0;

}

void turnLeft(int speed, int degrees10) {
	int error = 5;
	while(abs(SensorValue[gyro]) > degrees10 + error || abs(SensorValue[gyro]) < degrees10 - error) {
		motor[RightBase] = speed;
		motor[LeftBase]  = -speed;
	}
	motor[RightBase] = 0;
	motor[LeftBase]  = 0;
}

void turnRight(int speed, int degrees10) {
	int error = 5;
	while(abs(SensorValue[gyro]) > degrees10 + error || abs(SensorValue[gyro]) < degrees10 - error) {
		motor[RightBase] = -speed;
		motor[LeftBase]  = speed;
	}
	motor[RightBase] = 0;
	motor[LeftBase]  = 0;
}

void openClaw() {
	motor[Claw] = 127;
	wait1Msec(500);
	motor[Claw] = 0;
}

void closeClaw() {
	motor[Claw] = -63;
	wait1Msec(500);
}

void driveLift(int speed, int time) {
	motor[Lift] = speed;
	wait1Msec(time);
	motor[Lift] = 0;
}
void driveClaw(int speed, int time) {
	motor[Claw] = speed;
	wait1Msec(time);
}

